function productOrSum(num1, num2) {
    // Check if the two numbers are the same
    // If they are the same, return their product
    // If they are different, return their sum
    return num1 === num2 ? num1 * num2 : num1 + num2;
}

// Example:
console.log(productOrSum(9, 9)); 
console.log(productOrSum(8, 4)); 